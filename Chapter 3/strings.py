# | Function                | Description                                       | Example                                 |
# | ----------------------- | ------------------------------------------------- | --------------------------------------- |
# | `len()`                 | Returns the length of the string                  | `len("hello") → 5`                      |
# | `lower()`               | Converts all characters to lowercase              | `"HELLO".lower() → "hello"`             |
# | `upper()`               | Converts all characters to uppercase              | `"hello".upper() → "HELLO"`             |
# | `strip()`               | Removes leading and trailing whitespaces          | `"  hello  ".strip() → "hello"`         |
# | `lstrip()` / `rstrip()` | Removes leading / trailing spaces                 | `"  hello".lstrip() → "hello"`          |
# | `replace(old, new)`     | Replaces substring with another                   | `"hello".replace("l", "x") → "hexxo"`   |
# | `split(sep)`            | Splits string into a list                         | `"a,b,c".split(",") → ['a', 'b', 'c']`  |
# | `join(list)`            | Joins list elements into a string                 | `" ".join(['a', 'b']) → "a b"`          |
# | `find(sub)`             | Returns first index of substring, -1 if not found | `"hello".find("l") → 2`                 |
# | `index(sub)`            | Same as `find()` but raises error if not found    | `"hello".index("l") → 2`                |
# | `startswith(prefix)`    | Checks if string starts with given prefix         | `"hello".startswith("he") → True`       |
# | `endswith(suffix)`      | Checks if string ends with given suffix           | `"hello".endswith("lo") → True`         |
# | `isalpha()`             | Checks if all chars are alphabetic                | `"hello".isalpha() → True`              |
# | `isdigit()`             | Checks if all chars are digits                    | `"123".isdigit() → True`                |
# | `isalnum()`             | Checks if all chars are alphanumeric              | `"abc123".isalnum() → True`             |
# | `isspace()`             | Checks if string contains only whitespace         | `"  ".isspace() → True`                 |
# | `title()`               | Converts string to title case                     | `"hello world".title() → "Hello World"` |
# | `capitalize()`          | Capitalizes first letter of the string            | `"python".capitalize() → "Python"`      |
# | `count(sub)`            | Counts how many times a substring appears         | `"banana".count("a") → 3"`              |
